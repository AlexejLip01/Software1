# Ввод координат двух клеток
x1 = int(input("Введите x координату первой клетки: "))
y1 = int(input("Введите y координату первой клетки: "))
x2 = int(input("Введите x координату второй клетки: "))
y2 = int(input("Введите y координату второй клетки: "))

# Определение цвета клетки
def get_color(x, y):
    # Если сумма координат четная - белая, нечетная - черная
    if (x + y) % 2 == 0:
        return "White"
    else:
        return "Black"

# Определяем цвета обеих клеток
color1 = get_color(x1, y1)
color2 = get_color(x2, y2)

# Проверяем, одинакового ли цвета клетки
if color1 == color2:
    print("YES")
    print(color1)  # Выводим их общий цвет
else:
    print("NO")


